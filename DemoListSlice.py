# List slicing
# Indexing starts from 0

my_list = ['p', 'r', 'o', 'g', 'r', 'a', 'm', 'i', 'z']

# print  o , g, r
print(my_list[2:5])

# print beginning to 4th letter 'g'
print(my_list[:4])

# print last letter .starts from -1
print(my_list[-1])

# print first letter
print(my_list[-9])

# print from o to end of list
print(my_list[2:])

# elements beginning to 4th
print(my_list[:-5])

# elements 6th to end
print(my_list[5:])

# elements beginning to end
print(my_list[:])